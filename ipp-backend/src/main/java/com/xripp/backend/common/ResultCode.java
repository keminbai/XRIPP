package com.xripp.backend.common;

import lombok.Getter;

/**
 * 统一状态码枚举
 *
 * @author XRIPP Team
 * @since 2026-02-08
 *
 * 状态码规范:
 * - 0: 成功
 * - 400-499: 客户端错误
 * - 500-599: 服务端错误
 * - 1000+: 业务错误
 */
@Getter
public enum ResultCode {

    // ========================================================================
    // 通用状态码 (0-99)
    // ========================================================================

    SUCCESS(0, "操作成功"),
    ERROR(500, "操作失败"),

    // ========================================================================
    // 客户端错误 (400-499)
    // ========================================================================

    BAD_REQUEST(400, "请求参数错误"),
    UNAUTHORIZED(401, "未授权，请先登录"),
    FORBIDDEN(403, "权限不足，禁止访问"),
    NOT_FOUND(404, "请求的资源不存在"),
    METHOD_NOT_ALLOWED(405, "请求方法不支持"),
    REQUEST_TIMEOUT(408, "请求超时"),

    // ========================================================================
    // 服务端错误 (500-599)
    // ========================================================================

    INTERNAL_SERVER_ERROR(500, "服务器内部错误"),
    SERVICE_UNAVAILABLE(503, "服务暂不可用"),
    GATEWAY_TIMEOUT(504, "网关超时"),

    // ========================================================================
    // 认证相关 (1000-1099)
    // ========================================================================

    TOKEN_EXPIRED(1001, "Token已过期，请重新登录"),
    TOKEN_INVALID(1002, "Token无效"),
    TOKEN_MISSING(1003, "请求头缺少Token"),
    LOGIN_FAILED(1010, "用户名或密码错误"),
    ACCOUNT_DISABLED(1011, "账号已被禁用"),
    ACCOUNT_LOCKED(1012, "账号已被锁定"),

    // ========================================================================
    // 权限相关 (1100-1199)
    // ========================================================================

    PERMISSION_DENIED(1100, "权限不足"),
    ROLE_NOT_ALLOWED(1101, "当前角色无权访问"),
    DATA_SCOPE_VIOLATION(1102, "数据越权访问"),

    // ========================================================================
    // 业务错误 (2000-2999)
    // ========================================================================

    // 会员相关 (2000-2099)
    MEMBER_NOT_FOUND(2001, "会员不存在"),
    MEMBER_ALREADY_EXISTS(2002, "会员已存在"),

    // 合伙人相关 (2100-2199)
    PARTNER_NOT_FOUND(2101, "合伙人不存在"),
    PARTNER_BENEFIT_INSUFFICIENT(2102, "合伙人权益余额不足"),

    // 权益相关 (2200-2299)
    BENEFIT_INSUFFICIENT(2201, "权益余额不足"),
    BENEFIT_GRANT_FAILED(2202, "权益发放失败"),
    BENEFIT_TYPE_INVALID(2203, "权益类型无效"),

    // 审核相关 (2300-2399)
    AUDIT_STATUS_INVALID(2301, "审核状态无效"),
    AUDIT_NOT_ALLOWED(2302, "当前状态不允许审核"),
    AUDIT_PERMISSION_DENIED(2303, "无审核权限"),

    // 供应商相关 (2400-2499)
    SUPPLIER_NOT_FOUND(2401, "供应商不存在"),

    // 活动相关 (2500-2599)
    ACTIVITY_NOT_FOUND(2501, "活动不存在"),
    ACTIVITY_FULL(2502, "活动报名已满"),
    ACTIVITY_EXPIRED(2503, "活动已过期"),

    // ========================================================================
    // 数据库错误 (3000-3099)
    // ========================================================================

    DB_ERROR(3000, "数据库操作失败"),
    DB_DUPLICATE_KEY(3001, "数据已存在，不能重复添加"),
    DB_FOREIGN_KEY_VIOLATION(3002, "数据被其他记录引用，不能删除"),

    // ========================================================================
    // 文件相关 (4000-4099)
    // ========================================================================

    FILE_UPLOAD_FAILED(4001, "文件上传失败"),
    FILE_SIZE_EXCEED(4002, "文件大小超过限制"),
    FILE_TYPE_NOT_ALLOWED(4003, "文件类型不支持");

    // ========================================================================
    // 枚举字段
    // ========================================================================

    /**
     * 状态码
     */
    private final Integer code;

    /**
     * 提示信息
     */
    private final String msg;

    /**
     * 构造方法
     */
    ResultCode(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }
}